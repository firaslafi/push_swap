#include "ft_printf.h"

int	ft_putnbr_fd_unsigned(unsigned int n, int fd)
{
	char	c;

	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	c = (n % 10) + '0';
	ft_putchar_fd(c, fd);
	return (1);
}
